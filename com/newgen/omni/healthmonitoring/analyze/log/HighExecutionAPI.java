package com.newgen.omni.healthmonitoring.analyze.log;




import java.util.ArrayList;
import java.util.LinkedList;

public class HighExecutionAPI {
    private String APIName;
    private LinkedList<SubHighExecutionAPI> list;
    private Integer minTime;
    private Integer maxTime;
    private Integer noOfExecution;
    private Integer average;
    private Integer totalSum;
    
    public HighExecutionAPI(String APIName, LinkedList<SubHighExecutionAPI> list, Integer minTime, Integer maxTime, Integer noOfExecution, Integer average, Integer totalSum){
        this.APIName = APIName;
        this.list = list;
        this.minTime = minTime;
        this.maxTime = maxTime;
        this.noOfExecution = noOfExecution;
        this.average  = average;
        this.totalSum = totalSum;
    }
    
    public void setAPIName(String APIName){
        this.APIName = APIName;
    }
    public String getAPIName(){
        return APIName;
    }
    
    public void setArrayList(LinkedList<SubHighExecutionAPI> list){
        this.list = list;
    }
    public LinkedList<SubHighExecutionAPI> getArrayList(){
        return list;
    }
    
    public void setMinTime(Integer minTime){
        this.minTime = minTime;
    }
    public Integer getMinTime(){
        return minTime;
    }
    
    public void setMaxTime(Integer maxTime){
        this.maxTime = maxTime;
    }
    public Integer getMaxTime(){
        return maxTime;
    }
    
    public void setNoOfExecution(Integer noOfExecution){
        this.noOfExecution = noOfExecution;
    }
    public Integer getNoOfExecution(){
        return noOfExecution;
    }
    
    public void setAverage(Integer average){
        this.average = average;
    }
    public Integer getAverage(){
        return average;
    }
    
    public void setTotalSum(Integer totalSum){
        this.totalSum = totalSum;
    }
    public Integer getTotalSum(){
        return totalSum;
    }
}
