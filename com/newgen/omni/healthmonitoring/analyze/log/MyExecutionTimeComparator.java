package com.newgen.omni.healthmonitoring.analyze.log;




import java.util.Comparator;

public class MyExecutionTimeComparator implements Comparator<SubHighExecutionAPI> {
    @Override
    public int compare(SubHighExecutionAPI obj1, SubHighExecutionAPI obj2){
        return obj2.getExecutionTime()- obj1.getExecutionTime();
    }
}
