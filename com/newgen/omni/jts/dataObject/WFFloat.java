package com.newgen.omni.jts.dataObject;


public class WFFloat implements java.io.Serializable{
	private float value;

	public WFFloat() {
    }

	public WFFloat(float value) {
		this.value = value;
    }

    public float getValue() {
		return value;
    }

    public void setValue(float value) {
		this.value = value;
    }

	public String toString() {
		return String.valueOf(value);
	}
}