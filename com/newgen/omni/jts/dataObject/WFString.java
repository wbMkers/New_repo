package com.newgen.omni.jts.dataObject;


public class WFString implements java.io.Serializable{
	private String value;

	public WFString() {
    }

	public WFString(String value) {
		this.value = value;
    }

    public String getValue() {
		return value;
    }

    public void setValue(String value) {
		this.value = value;
    }

	public String toString() {
		if (value == null) {
			return "null";
		}else {
			return value;
		}
	}
}